def compress_image(input_path, target_size_mb=1.0, quality=95):
    """
    Сжимает изображение. Добавлена проверка на 0 байт.
    """
    try:
        if not os.path.exists(input_path) or os.path.getsize(input_path) == 0:
            return input_path

        # Генерируем имя для сжатого файла
        filename = os.path.basename(input_path)
        name, ext = os.path.splitext(filename)
        output_path = os.path.join(TEMP_DIR, f"{name}_compressed.jpg")

        # Используем контекстный менеджер
        with Image.open(input_path) as img:
            img = img.convert("RGB") # Убираем прозрачность для JPG
            
            # Первичное сохранение
            img.save(output_path, "JPEG", quality=quality, optimize=True)
            
            # Цикл уменьшения качества
            while os.path.getsize(output_path) / (1024 * 1024) > target_size_mb and quality > 30:
                quality -= 10
                img.save(output_path, "JPEG", quality=quality, optimize=True)
        
        # ВАЖНАЯ ПРОВЕРКА: Если сжатый файл получился 0 байт — возвращаем оригинал
        if os.path.getsize(output_path) == 0:
            logger.error("Compression failed (0 bytes), using original.")
            return input_path
            
        return output_path
    except Exception as e:
        logger.error(f"Ошибка сжатия: {e}")
        return input_path # Если ошибка - возвращаем оригинал

