async def send_with_retry(method, chat_id, file_path, caption, reply_markup, retries=3):
    """
    Умная отправка: если сеть падает, чинит бота и пробует снова.
    method: bot.send_photo или bot.send_document
    """
    current_try = 0
    while current_try < retries:
        try:
            # Создаем объект файла заново при каждой попытке (важно для aiohttp!)
            file_obj = FSInputFile(file_path)
            
            # Таймаут 60 сек, чтобы успело загрузиться большое фото
            await method(chat_id, file_obj, caption=caption, reply_markup=reply_markup, request_timeout=60)
            return True
            
        except TelegramRetryAfter as e:
            logger.warning(f"⏳ FloodWait: жду {e.retry_after} сек...")
            await asyncio.sleep(e.retry_after)
            current_try += 1 # FloodWait не считается за ошибку сети, но счетчик увеличим
            
        except (TelegramNetworkError, Exception) as e:
            logger.error(f"❌ Ошибка отправки (Попытка {current_try+1}/{retries}): {e}")
            
            # --- ЛЕЧЕНИЕ СЕТИ ---
            logger.warning("🚑 Запускаю лечение сети...")
            await fix_telegram_network() 
            await asyncio.sleep(3) # Даем время на переподключение
            current_try += 1
            
    return False

