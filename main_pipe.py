async def save_history_files(user_id, file_paths, category):
    """Сохраняет пакет файлов как 'прошлую генерацию' для групп/пар"""
    if not file_paths: return

    # Определяем, куда сохранять (разделим логику для одиночных и групп)
    is_group_mode = category in ["group", "joint"]
    
    saved_paths = []
    
    # Копируем файлы в постоянное хранилище (чтобы temp не удалился)
    for idx, src in enumerate(file_paths):
        if os.path.exists(src):
            # Имя файла: last_group_ID_0.jpg, last_group_ID_1.jpg и т.д.
            prefix = "last_group" if is_group_mode else "last_single"
            dst = os.path.join(TEMP_DIR, f"{prefix}_{user_id}_{idx}.jpg")
            try:
                await asyncio.to_thread(shutil.copy, src, dst)
                saved_paths.append(dst)
            except Exception as e:
                logger.error(f"Save history error: {e}")

    # Обновляем JSON пользователя
    async with users_lock:
        users = await asyncio.to_thread(_load_users_sync)
        str_id = str(user_id)
        if str_id in users:
            if is_group_mode:
                users[str_id]["last_group_photos"] = saved_paths # Список для групп
            else:
                if saved_paths:
                    users[str_id]["last_photo"] = saved_paths[0] # Одиночное фото
            
            await asyncio.to_thread(_save_users_sync, users)

