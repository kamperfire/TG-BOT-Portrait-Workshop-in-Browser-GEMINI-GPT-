MAINTENANCE_FILE = "maintenance.json"

class MaintenanceManager:
    def __init__(self):
        self.active = False
        self.end_time = 0
        self.duration_minutes = 0
        self.bonus_amount = 0
        self.affected_users = {} # {chat_id: message_id}
        self.load_state()

    def load_state(self):
        if os.path.exists(MAINTENANCE_FILE):
            try:
                with open(MAINTENANCE_FILE, 'r') as f:
                    data = json.load(f)
                    self.active = data.get("active", False)
                    self.end_time = data.get("end_time", 0)
                    self.duration_minutes = data.get("duration_minutes", 0)
                    self.affected_users = data.get("affected_users", {})
                    # Если время вышло пока бот спал
                    if self.active and time.time() > self.end_time:
                        asyncio.create_task(self.stop_maintenance(auto=True))
            except:
                pass

    def save_state(self):
        data = {
            "active": self.active,
            "end_time": self.end_time,
            "duration_minutes": self.duration_minutes,
            "affected_users": self.affected_users
        }
        with open(MAINTENANCE_FILE, 'w') as f:
            json.dump(data, f)

    def start_maintenance(self, minutes):
        self.active = True
        self.duration_minutes = minutes
        self.end_time = time.time() + (minutes * 60)
        self.bonus_amounts = {5: 5, 10: 10, 30: 15, 60: 30}
        self.bonus_amount = self.bonus_amounts.get(minutes, 10)
        self.affected_users = {}
        self.save_state()

    async def stop_maintenance(self, auto=False):
        if not self.active: return
        
        actual_bonus = self.bonus_amount if auto else 10 # 10р если ручная отмена
        self.active = False
        self.save_state()
        
        # Рассылка уведомлений и бонусов
        for chat_id_str, msg_id in list(self.affected_users.items()):
            try:
                chat_id = int(chat_id_str)
                await update_balance(chat_id, actual_bonus)
                
                text = (
                    f"✅ **Технические работы завершены!**\n"
                    f"Спасибо за ожидание.\n"
                    f"🎁 Вам начислен бонус: **{actual_bonus} ₽**"
                )
                await bot.edit_message_text(text, chat_id=chat_id, message_id=msg_id, parse_mode="Markdown", reply_markup=main_menu_kb(get_user(chat_id, "")['balance'], chat_id))
            except Exception as e:
                logger.error(f"Maintenance notify error: {e}")
        
        self.affected_users = {}
        self.save_state()

    def get_remaining_time(self):
        diff = int(self.end_time - time.time())
        if diff < 0: return "00:00"
        return str(datetime.timedelta(seconds=diff))

    def register_user_attempt(self, chat_id, message_id):
        self.affected_users[str(chat_id)] = message_id
        self.save_state()

