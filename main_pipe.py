async def is_maintenance_active(event, state: FSMContext):
    """Возвращает True если работы идут и отправляет сообщение"""
    if maintenance.active:
        # Проверка таймера (вдруг истек только что)
        if time.time() > maintenance.end_time:
            await maintenance.stop_maintenance(auto=True)
            return False
            
        text = (
            f"🛠 **ТЕХНИЧЕСКИЕ РАБОТЫ**\n\n"
            f"Бот находится на обслуживании.\n"
            f"⏳ До окончания: **{maintenance.get_remaining_time()}**\n\n"
            f"Пожалуйста, подождите. После завершения вам будет начислен бонус!"
        )
        
        user_id = event.from_user.id
        msg = None
        
        if isinstance(event, types.CallbackQuery):
            # Если это кнопка "Обновить"
            if event.data == "refresh_maintenance":
                try:
                    await event.message.edit_text(text, parse_mode="Markdown", reply_markup=maintenance_kb())
                except: pass
                await event.answer("Время обновлено")
                return True
            
            # Иначе скрываем старое и шлем новое
            await event.message.delete()
            msg = await event.message.answer(text, parse_mode="Markdown", reply_markup=maintenance_kb())
            await event.answer()
        else:
            msg = await event.answer(text, parse_mode="Markdown", reply_markup=maintenance_kb())

        # Регистрируем юзера для получения бонуса потом
        if msg:
            maintenance.register_user_attempt(user_id, msg.message_id)
            
        return True
    return False

