async def run_broadcast_loop(status_msg, users, bc_type, file_id, text):
    """Фоновая функция рассылки"""
    success = 0
    blocked = 0
    total = len(users)
    i = 0
    
    start_time = time.time()
    
    for user_id_str in users:
        i += 1
        user_id = int(user_id_str)
        
        try:
            if bc_type == "photo":
                await bot.send_photo(user_id, file_id, caption=text)
            else:
                await bot.send_message(user_id, text)
            success += 1
        except Exception as e:
            # Обычно это 'Forbidden: bot was blocked by the user'
            blocked += 1
        
        # Обновляем статус каждые 20 сообщений
        if i % 20 == 0:
            await asyncio.sleep(1) # Пауза чтобы не забанил ТГ (flood control)
            try:
                await status_msg.edit_text(f"🚀 Рассылка: {i}/{total}\n✅ Успешно: {success}\n💀 Блок: {blocked}")
            except: pass
            
    duration = int(time.time() - start_time)
    try:
        await status_msg.edit_text(
            f"🏁 **Рассылка завершена!**\n"
            f"⏱ Время: {duration} сек.\n"
            f"✅ Доставлено: {success}\n"
            f"💀 Недоступно: {blocked}"
        )
    except: pass

